/**
 * Get a sorted array of unique category names from the master specifications list
 * @returns Sorted array of category names
 */
export function getSpecCategories(): string[] {
  const categories = new Set(masterSpecifications.map(spec => spec.category));
  return Array.from(categories).sort();
}

/**
 * Filter specifications by category
 * @param category - The category name to filter by
 * @returns Array of specifications matching the category
 */
export function getSpecsByCategory(category: string): SpecItem[] {
  return masterSpecifications.filter(spec => spec.category === category);
}

/**
 * Get a specification by ID
 * @param id - The specification ID to find
 * @returns The specification item or undefined if not found
 */
export function getSpecById(id: string): SpecItem | undefined {
  return masterSpecifications.find(spec => spec.id === id);
}

/**
 * Search specifications by keyword (searches across subCategory, description, and manufacturer)
 * @param keyword - The search term
 * @returns Array of specifications matching the search term
 */
export function searchSpecs(keyword: string): SpecItem[] {
  const searchTerm = keyword.toLowerCase();
  return masterSpecifications.filter(spec =>
    spec.subCategory.toLowerCase().includes(searchTerm) ||
    spec.description.toLowerCase().includes(searchTerm) ||
    (spec.manufacturer && spec.manufacturer.toLowerCase().includes(searchTerm))
  );
}
